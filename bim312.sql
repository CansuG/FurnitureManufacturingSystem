CREATE DATABASE bim312;
USE bim312;

CREATE TABLE Employee(
SSN char(9) NOT NULL,
FName varchar(25) NOT NULL,
MName varchar(25),
LName varchar(25) NOT NULL,
ETelNum varchar(15) NOT NULL,
Salary int NOT NULL,
EAddress varchar(255) NOT NULL,
Gender varchar(25) NOT NULL,
BDate date NOT NULL,
StartDate date NOT NULL,
Com_ID char(5) NOT NULL,
PRIMARY KEY (SSN)
);

CREATE TABLE Company(
Com_ID char(5) NOT NULL,
Email varchar(35) NOT NULL,
CompTel varchar(15) NOT NULL,
ComName varchar(25) NOT NULL,
ComAddress varchar(255) NOT NULL,
PRIMARY KEY (Com_ID)
);

ALTER TABLE Employee
ADD FOREIGN KEY(Com_ID) REFERENCES Company(Com_ID);

CREATE TABLE Furniture(
F_ID char(7) NOT NULL,
Quantity int NOT NULL,
Price int NOT NULL,
FType varchar(50) NOT NULL,
Mdate date NOT NULL,
Com_ID char(5) NOT NULL,
PRIMARY KEY (F_ID),
FOREIGN KEY (Com_ID) REFERENCES Company(Com_ID)
);

CREATE TABLE FurnitureColor(
F_ID char(7) NOT NULL,
Color varchar(25) NOT NULL,
CONSTRAINT PKColor PRIMARY KEY(F_ID,Color),
FOREIGN KEY (F_ID) REFERENCES Furniture(F_ID)
); 
CREATE TABLE Clientt(
Cl_ID char(6) NOT NULL,
ClEmail varchar(40) NOT NULL,
ClCity varchar(20) NOT NULL,
ClFullAddress varchar(100) NOT NULL,
ClTelNum varchar(15) NOT NULL,
ClName varchar(30) NOT NULL,
PRIMARY KEY(Cl_ID)
);

CREATE TABLE Orderr(
 Order_ID char(5) NOT NULL,
 Cl_ID char(6) NOT NULL,
OrderDate date NOT NULL,
CONSTRAINT PKOrder PRIMARY KEY(Order_ID, Cl_ID),
FOREIGN KEY(Cl_ID) REFERENCES Clientt(Cl_ID)
);

CREATE TABLE SoldAs(
F_ID char(7) NOT NULL, 
Order_ID char(5) NOT NULL,
Q_Quantity int NOT NULL,
CONSTRAINT PKSold PRIMARY KEY(F_ID, Order_ID),
FOREIGN KEY(F_ID) REFERENCES Furniture(F_ID),
FOREIGN KEY(Order_ID) REFERENCES Orderr(Order_ID)
);

